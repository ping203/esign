import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IMsgSubTask } from '@/shared/model/msg-sub-task.model';

const baseApiUrl = 'api/msg-sub-tasks';

export default class MsgSubTaskService {
  public find(id: number): Promise<IMsgSubTask> {
    return new Promise<IMsgSubTask>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMsgSubTask): Promise<IMsgSubTask> {
    return new Promise<IMsgSubTask>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IMsgSubTask): Promise<IMsgSubTask> {
    return new Promise<IMsgSubTask>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
