import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IApiInvokeLog } from '@/shared/model/api-invoke-log.model';

const baseApiUrl = 'api/api-invoke-logs';

export default class ApiInvokeLogService {
  public find(id: number): Promise<IApiInvokeLog> {
    return new Promise<IApiInvokeLog>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IApiInvokeLog): Promise<IApiInvokeLog> {
    return new Promise<IApiInvokeLog>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IApiInvokeLog): Promise<IApiInvokeLog> {
    return new Promise<IApiInvokeLog>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
